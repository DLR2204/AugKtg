package intro;

public class VariableEx1 {
	
	static int number = 56; //static global variable
	
	int value =45;   //instance global variable
	
	
	public static void main(String[] args) {
		
		
		byte age = 34; //local variable
		
		int number = 7867867;
		
		float pi = 3.14f;
		
		boolean value = true;
		
		char name = 'f';
		
		System.out.println(age);
		
		System.out.println(number);
		
		System.out.println(pi);
		
		System.out.println(value);
		
		System.out.println(name);
		
		
		
	}

}
